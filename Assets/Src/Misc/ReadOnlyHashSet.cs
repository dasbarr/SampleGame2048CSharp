using System.Collections;
using System.Collections.Generic;

namespace SampleGame2048 {
    
    /// <summary>
    /// Readonly version of the HashSet.
    /// </summary>
    /// <typeparam name="T">HashSet item type.</typeparam>
    public class ReadOnlyHashSet<T> : IReadOnlyCollection<T> {
        
        //-------------------------------------------------------------
        // Nested
        //-------------------------------------------------------------
        
        //-------------------------------------------------------------
        // Dependencies
        //-------------------------------------------------------------
        
        //-------------------------------------------------------------
        // Class constants
        //-------------------------------------------------------------
        
        //-------------------------------------------------------------
        // Class variables
        //-------------------------------------------------------------
        
        //-------------------------------------------------------------
        // Class methods
        //-------------------------------------------------------------
        
        //-------------------------------------------------------------
        // Constructor/finalizer
        //-------------------------------------------------------------

        public ReadOnlyHashSet(HashSet<T> source) {
            _source = source;
        }
        
        //-------------------------------------------------------------
        // Variables
        //-------------------------------------------------------------

        private readonly HashSet<T> _source;

        //-------------------------------------------------------------
        // Properties
        //-------------------------------------------------------------

        /// <inheritdoc />
        public int Count => _source.Count;
        
        //-------------------------------------------------------------
        // Private properties serialized for Unity
        //-------------------------------------------------------------

        //-------------------------------------------------------------
        // Events
        //-------------------------------------------------------------

        //-------------------------------------------------------------
        // Public methods
        //-------------------------------------------------------------

        /// <summary>
        /// Checks if source HashSet contains an item or not.
        /// </summary>
        /// <param name="item">Item to check.</param>
        /// <returns>True if contains, false otherwise.</returns>
        public bool Contains(T item) => _source.Contains(item);
        
        /// <inheritdoc />
        public IEnumerator<T> GetEnumerator() => _source.GetEnumerator();
        /// <inheritdoc />
        IEnumerator IEnumerable.GetEnumerator() => _source.GetEnumerator();

        //-------------------------------------------------------------
        // Protected methods
        //-------------------------------------------------------------

        //-------------------------------------------------------------
        // Private methods
        //-------------------------------------------------------------

        //-------------------------------------------------------------
        // Unity methods
        //-------------------------------------------------------------
    }
}